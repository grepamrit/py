# using math modules
import math 

# Normal Operation 
print("\nThis is a Normal Operation:")
print(10/3)  # Float Division 
print(10//3) # Integer Division 
print("\n")

# using floor 
print("This is a floor value:")
print(math.floor(10/3))
print("\n")

# using ceil 
print("This is a ceil value:")
print(math.ceil(10/3))
print("\n")

# using round
print("Using round Operations: ")
print(round(10/3))  
print(round(10/3,4)) # this prints out the 4 digit after decimal
print(round(20/3,2))
